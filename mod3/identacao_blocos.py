def sacar(valor): # Definição da função sacar com o parametro valor

    saldo = 1000  # Exemplo de saldo inicial

    if saldo >= valor:
        print("Valor sacado!")

    print("Obrigado por usar nosso caixa eletrônico!")  # Mensagem de agradecimento

sacar(100) # Chamada da função sacar com o valor 100, que é o argumento passado para a função