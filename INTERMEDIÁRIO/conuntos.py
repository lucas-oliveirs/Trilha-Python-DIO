set([1,2,3,1,3,4]) # criando um conjunto a partir de uma lista, elementos duplicados são removidos

set("banana") # criando um conjunto a partir de uma string, letras duplicadas são removidas

set(("palio", "gol", "celta", "palio")) # criando um conjunto a partir de uma tupla, elementos duplicados são removidos

linguagens = {"python", "java", "c++", "python"} # criando um conjunto diretamente, elementos duplicados são removidos
print(linguagens) # saída: {'c++', 'java', 'python'}